def train(X, Y, layers_dims, learning_rate, num_iterations, print_cost=True, fcost='x-entropy'):
    """
    Implements a L-layer neural network: [LINEAR->RELU]*(L-1)->LINEAR->SIGMOID.

    X: data, array of shape (num_px * num_px * 3, number of examples)
    Y: true "label" vector 
    layers_dims: list containing input size and each layer size
    learning_rate
    num_iterations: number of iterations of the optimization loop
    print_cost: if True, it prints the cost every 100 steps
    parameters: parameters learnt by the model
    """
    costs = []                         # keep track of cost
    
    # Parameters initialization
    parameters = initialize_parameters_deep(layers_dims)
    
    # Loop (gradient descent)
    for i in range(0, num_iterations):

        # Forward propagation
        AL, caches = L_model_forward(X,parameters)
        # Compute cost
        if fcost == 'x-entropy':
            cost = compute_cost(AL,Y)
        elif fcost == 'mse':
            cost = compute_cost_MSE(AL,Y)
        # Backward propagation.
        grads = L_model_backward(AL,Y,caches, fcost)
        # Update parameters.
        parameters = update_parameters(parameters,grads,learning_rate)
                
        # Print the cost every 100 training example
        if print_cost and i % 100 == 0:
            print ("Cost after iteration %i: %f" %(i, cost))
        if print_cost and i % 100 == 0:
            costs.append(cost)
            
    # plot the cost
    plt.rcParams['figure.figsize'] = (5.0, 4.0) # set default size of plots
    plt.figure()
    plt.plot(np.squeeze(costs))
    plt.ylabel('cost')
    plt.xlabel('Iterations/100')
    plt.title("Learning rate =" + str(learning_rate))
    
    
    return parameters





def sigmoid(Z):
    """
    Implements the sigmoid activation in numpy
    
    Arguments:
    Z -- numpy array of any shape
    
    Returns:
    A -- output of sigmoid(z), same shape as Z
    cache -- returns Z as well, useful during backpropagation
    """
    
    A = 1/(1+np.exp(-Z))
    cache = Z
    
    return A, cache

def relu(Z):
    """
    Implement the RELU function.

    Arguments:
    Z -- Output of the linear layer, of any shape

    Returns:
    A -- Post-activation parameter, of the same shape as Z
    cache -- a python dictionary containing "A" ; stored for computing the backward pass efficiently
    """
    
    A = np.maximum(0,Z)
    
    assert(A.shape == Z.shape)
    
    cache = Z 
    return A, cache

